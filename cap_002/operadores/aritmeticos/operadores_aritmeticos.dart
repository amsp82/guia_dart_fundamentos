void main() {
  print(40 + 2); // Adição
  print('40' + '2'); // Concatenação
  print(50 - 8); // Subtração
  print(-(-42)); // -expression, inverte o sinal da operação
  print(6 * 7); // Multiplicação
  print(11 / 2); // Divisão
  print(11 ~/ 2); // Retorna o inteiro da divisão
  print(11 % 2); // Retorna o resto da divisão
}
