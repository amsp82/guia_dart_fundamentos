void main() {
  num a = 4; // Atribuição
  print(a);

  a += 5; // Adição e Atribuição
  print(a);

  a -= 4; // Subtração e Atribuição
  print(a);

  a %= 2; // Resto da divisão e Atribuição
  print(a);

  a *= 11; // Multiplicação e Atribuição
  print(a);

  a /= 5; // Divisão e Atribuição
  print(a);

  a ~/= 2; // Retorna o valor inteiro da divisão e Atribuição
  print(a);
}
